/**
 * @brief Converts a JSON Schema type string to a protobuf type.
 */
QString API::GRPC::ProtoGenerator::jsonTypeToProtoType(const QString& jsonType)
{
  // Map JSON Schema type to protobuf field type
  if (jsonType == QStringLiteral("string"))
    return QStringLiteral("string");
  if (jsonType == QStringLiteral("number"))
    return QStringLiteral("double");
  if (jsonType == QStringLiteral("integer"))
    return QStringLiteral("int64");
  if (jsonType == QStringLiteral("boolean"))
    return QStringLiteral("bool");
  if (jsonType == QStringLiteral("array"))
    return QStringLiteral("google.protobuf.ListValue");

  // object, mixed, or unknown → use Struct
  return QStringLiteral("google.protobuf.Struct");
}

/**
 * @brief Sanitizes a dot-notation command name to a valid protobuf identifier.
 *
 * Converts "io.driver.uart.setBaudRate" to "IoDriverUartSetBaudRate".
 */
QString API::GRPC::ProtoGenerator::sanitizeName(const QString& name)
{
  // Convert dot-notation to CamelCase
  QString result;
  bool capitalize = true;

  for (const auto& ch : name) {
    if (ch == '.' || ch == '_' || ch == '-') {
      capitalize = true;
      continue;
    }

    if (capitalize) {
      result += ch.toUpper();
      capitalize = false;
    } else {
      result += ch;
    }
  }

  return result;
}

